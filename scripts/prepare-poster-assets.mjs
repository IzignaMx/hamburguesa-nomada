/**
 * prepare-poster-assets.mjs
 *
 * Optimiza los PNG oficiales del póster para web:
 *  - Recorta márgenes transparentes
 *  - Genera WebP con alpha
 *  - Guarda reporte de pesos
 *
 * Uso: node scripts/prepare-poster-assets.mjs
 */

import { readFileSync, writeFileSync, mkdirSync, copyFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import sharp from "sharp";

const { fileURLToPath } = await import("node:url");
const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const SRC_DIR = "D:\\MEGA\\hm-alleycat";
const ASSETS_DIR = join(ROOT, "src", "assets", "poster");
const ORIG_DIR = join(ASSETS_DIR, "originals");
const OUT_DIR = join(ASSETS_DIR, "generated");
const BRAND_DIR = join(ROOT, "src", "assets", "brand");

const ASSETS = [
  {
    id: "title",
    src: "AlleyCat_HamburguesaNomada-titulo.png",
    name: "poster-title-composition",
    maxW: 1200,
    desc: "Composición principal del título",
  },
  {
    id: "rays",
    src: "AlleyCat_HamburguesaNomada-rayos.png",
    name: "poster-rays-flames",
    maxW: 800,
    desc: "Rayos y flamas decorativos",
  },
  {
    id: "frame",
    src: "AlleyCat_HamburguesaNomada-recuadro.png",
    name: "poster-frame",
    maxW: 600,
    desc: "Marco decorativo",
  },
  {
    id: "date",
    src: "AlleyCat_HamburguesaNomada-texto.png",
    name: "poster-date-place",
    maxW: 800,
    desc: "Texto curvo fecha y sede",
  },
];

mkdirSync(ORIG_DIR, { recursive: true });
mkdirSync(OUT_DIR, { recursive: true });
mkdirSync(BRAND_DIR, { recursive: true });

const report = [];
let totalOrig = 0;
let totalFinal = 0;

for (const asset of ASSETS) {
  const srcFile = join(SRC_DIR, asset.src);

  if (!existsSync(srcFile)) {
    report.push(`❌ ${asset.id}: no encontrado en ${srcFile}`);
    continue;
  }

  /* Leer metadata original */
  const meta = await sharp(srcFile).metadata();
  const origSize = (await sharp(srcFile).toBuffer()).length;
  totalOrig += origSize;

  /* Conservar original fuera de public */
  const origCopy = join(ORIG_DIR, asset.src);
  copyFileSync(srcFile, origCopy);

  /* Recortar transparencia */
  const trimmed = sharp(srcFile).trim({ threshold: 10 });

  const trimmedMeta = await trimmed.clone().metadata();
  const croppedW = trimmedMeta.width ?? meta.width;
  const croppedH = trimmedMeta.height ?? meta.height;

  /* Redimensionar si excede maxW */
  let resized = trimmed;
  if (croppedW > asset.maxW) {
    resized = trimmed.resize({ width: asset.maxW, withoutEnlargement: true });
  }

  /* Generar WebP con alpha */
  const webpFile = join(OUT_DIR, `${asset.name}.webp`);
  const webpBuf = await resized.clone().webp({ alphaQuality: 90, effort: 4 }).toBuffer();
  writeFileSync(webpFile, webpBuf);
  totalFinal += webpBuf.length;

  const savings = ((1 - webpBuf.length / origSize) * 100).toFixed(1);

  report.push(
    `✅ ${asset.id} — ${asset.desc}`,
    `   Original:  ${(origSize / 1024).toFixed(1)} KB  (${meta.width}×${meta.height})`,
    `   Recortado: ${croppedW}×${croppedH}`,
    `   WebP:      ${(webpBuf.length / 1024).toFixed(1)} KB`,
    `   Ahorro:    ${savings}%`,
    `   → ${webpFile}`,
    "",
  );
}

/* ── Logo (brand) ──────────────────────────── */

const logoFiles = [
  join(ROOT, "public", "logos", "hamburguesa-nomada.webp"),
  join(ROOT, "public", "logos", "hamburguesa-nomada.jpg"),
];

for (const logoFile of logoFiles) {
  if (existsSync(logoFile)) {
    const logoMeta = await sharp(logoFile).metadata();

    /* Guardar copia en brand/ */
    const brandExt = logoFile.endsWith(".webp") ? ".webp" : ".png";
    const brandFile = join(BRAND_DIR, `hamburguesa-nomada-logo${brandExt}`);
    copyFileSync(logoFile, brandFile);

    const logoSize = (await sharp(logoFile).toBuffer()).length;
    report.push(
      `✅ logo — Logotipo circular`,
      `   Original:  ${(logoSize / 1024).toFixed(1)} KB  (${logoMeta.width}×${logoMeta.height})`,
      `   → ${brandFile}`,
      "",
    );
    break;
  }
}

/* ── Reporte ───────────────────────────────── */

const totalSavings = ((1 - totalFinal / totalOrig) * 100).toFixed(1);

report.push(
  "─── RESUMEN ───",
  `Peso total original: ${(totalOrig / 1024).toFixed(1)} KB`,
  `Peso total final:    ${(totalFinal / 1024).toFixed(1)} KB`,
  `Ahorro total:        ${totalSavings}%`,
);

const reportPath = join(ROOT, "docs", "qa", "assets-v3.md");
const reportContent = `# Asset Optimization Report — V3

**Date:** ${new Date().toISOString().split("T")[0]}
**Script:** \`scripts/prepare-poster-assets.mjs\`

${report.join("\n")}

---

*Generated by prepare-poster-assets.mjs*
`;

writeFileSync(reportPath, reportContent, "utf-8");
console.log(`📝 Reporte guardado: ${reportPath}`);
console.log(`✅ Assets generados en: ${OUT_DIR}`);
